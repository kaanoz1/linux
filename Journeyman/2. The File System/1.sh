echo The File System Hierarchy
echo
echo 
echo    / - The root directory of the entire filesystem hierarchy; everything is nestled under this directory.
echo    /bin - Essential ready-to-run programs (binaries); includes the most basic commands such as ls and cp.
echo    /boot - Contains kernel boot loader files.
echo	/dev - Device files.
echo	/etc - Core system configuration directory; should hold only configuration files and not any binaries.
echo 	/home - Personal directories for users; holds your documents, files, settings, etc.
echo	/lib - Holds library files that binaries can use.
echo	/media - Used as an attachment point for removable media like USB drives.
echo	/mnt - Temporarily mounted filesystems.
echo	/opt - Optional application software packages.
echo	/proc - Information about currently running processes.
echo	"/root - The root user's home directory."
echo	/run - Information about the running system since the last boot.
echo	/sbin - Contains essential system binaries, usually can only be run by root.
echo	/srv - Site-specific data which are served by the system.
echo	/tmp - Storage for temporary files.
echo	"/usr - This is unfortunately named; most often it does not contain user files in the sense of a home folder. This is meant for user-installed software and utilities; however, that is not to say you can't add personal directories in there. Inside this directory are sub-directories for /usr/bin, /usr/local, etc."
echo	"/var - Variable directory; it's used for system logging, user tracking, caches, etc. Basically anything that is subject to change all the time."
echo
